n1 = input('Qual o valor que você quer analisar?')
print ('O número escolhido foi o \033[1;94m{}\033[m'.format(n1))
print (type(n1))
print ('É númerico? \033[1;94m{}\033[m'.format(n1.isnumeric()))
print ('É alfabético? \033[1;94m{}\033[m'.format(n1.isalpha()))
print ('É alfanumérico? \033[1;94m{}\033[m'.format(n1.isalnum()))
print ('Está apenas em letras maiúsculas? \033[1;94m{}\033[m'.format(n1.isupper()))
print ('Está apenas em letras minúsculas? \033[1;94m{}\033[m'.format(n1.islower()))
print ('Está capitalizado? \033[1;94m{}\033[m'.format(n1.istitle()))
print ('É composto apenas de espaços? \033[1;94m{}\033[m'.format(n1.isspace()))